#include<iostream>
#include<cmath>

using namespace std;

int main()
{
  cout << 3 + 5 << endl;
  cout << pow(2, 5) << endl;
  cout << sqrt(36) << endl;
  cout << round(3.6) << endl;
  cout << ceil(3.6) << endl;
  cout << floor(3.6) << endl;
  cout << fmax(3, 10) << endl;
  cout << fmin(3, 10) << endl;
  return 0;
}
